package com.lykke.tests.api.service.dashboardstatistics.models.leads;

import static java.util.stream.Collectors.toMap;

import com.fasterxml.jackson.annotation.JsonCreator;
import com.fasterxml.jackson.databind.PropertyNamingStrategy.UpperCamelCaseStrategy;
import com.fasterxml.jackson.databind.annotation.JsonNaming;
import java.util.Map;
import java.util.Optional;
import java.util.function.Function;
import java.util.stream.Stream;
import lombok.AllArgsConstructor;
import lombok.Getter;
import lombok.NoArgsConstructor;

@AllArgsConstructor
@NoArgsConstructor
@JsonNaming(UpperCamelCaseStrategy.class)
public enum LeadState {
    PENDING("Pending", "Lead is created and its pending confirmation"),
    CONFIRMED("Confirmed", "Lead is confirmed and its waiting approval"),
    APPROVED("Approved", "Lead is approved"),
    REJECTED("Rejected", "Lead is rejected");

    private static Map<String, LeadState> FORMAT_MAP =
            Stream.of(LeadState.values())
                    .collect(toMap(c -> c.getCode(), Function.identity()));
    @Getter
    private String code;

    @Getter
    private String comment;

    @JsonCreator
    public static LeadState fromString(String value) {
        return Optional
                .ofNullable(FORMAT_MAP.get(value))
                .orElseThrow(() -> new IllegalArgumentException(value));
    }
}
